package Methods;

public class STRING_02
{
    public static void main(String[] args)
    {
        String jesus = "Mexico is the champs";
        System.out.println("Name:"+ jesus);
        System.out.println("Uppercase:" + jesus.toUpperCase());
        System.out.println("Lowercase:" + jesus.toLowerCase());
        System.out.println("Length:" + jesus.length());
        System.out.println("Char:"+ jesus.charAt(10));
        System.out.println("Substring:" + jesus.substring(5,9));

    }
}
